    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (nom.getText().isEmpty() || ape.getText().isEmpty() || docu.getText().isEmpty() || ciuda.getText().isEmpty()|| corre.getText().isEmpty()|| direc.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
        } else {
       
       String []Datos = new String[6];
       Datos[0]=nom.getText();
       nom.setText(" ");
       Datos[1]=ape.getText();
       ape.setText(" ");
       Datos[2]=docu.getText();
       docu.setText(" ");
       Datos[3]=ciuda.getText();
       ciuda.setText(" ");
       Datos[4]=corre.getText();
       corre.setText(" ");
       Datos[5]=direc.getText();
       direc.setText(" ");
       dis.addRow(Datos);
       JOptionPane.showMessageDialog(null, "Los Datos registrados: ");
               }
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int filaseleccionada=jTable1.getSelectedRow();
        if (filaseleccionada>=0){
            dis.removeRow(filaseleccionada);
        }
        else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


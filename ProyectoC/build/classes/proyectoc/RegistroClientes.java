    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextNom.getText().isEmpty() || jTextApelli.getText().isEmpty() || jTextDocumento.getText().isEmpty() || jTextDirec.getText().isEmpty() ||jTextCorreo.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Los campos son obligatorios");
        } else {

            String[] Datos = new String[6];
            Datos[0] = jTextNom.getText();
            jTextNom.setText(" ");
            Datos[1] = jTextApelli.getText();
            jTextApelli.setText(" ");
            Datos[2] = jTextDocumento.getText();
            jTextDocumento.setText(" ");
            Datos[3] = jTextDirec.getText();
            jTextDirec.setText(" ");
            Datos[4]=jTextCorreo.getText();
            jTextCorreo.setText(" ");
            disen.addRow(Datos);

            Clientes ingreso = new Clientes();
            ingreso.setConta(Lingreso.size());
            Lingreso.add(ingreso);
            JOptionPane.showMessageDialog(null, "Los Datos Registrados: " + ingreso.getConta());

        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

